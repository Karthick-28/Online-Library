    public Search_mem_result(String c,String r) {
        initComponents();
        
        
        
        
        
        
        
        
            Connection conn;
            String JDBC_Driver;
            String DB_URL;
            JDBC_Driver = "com.mysql.jdbc.Driver";
            DB_URL = "jdbc:mysql://localhost:3306/library";
            try {
                Class.forName("com.mysql.jdbc.Driver");
                conn = (Connection) DriverManager.getConnection(DB_URL, "root", "");
                PreparedStatement stmt=null;
                textArea1.setEditable(false);
                switch(c)
                {
                    case "Name":
                 stmt = conn.prepareStatement("select * from member where Name = ? ");
                         stmt.setString(1,r );
       break;
       
                    case "Member ID":
                stmt = conn.prepareStatement("select * from member where Id = ? ");
                         stmt.setInt(1,Integer.parseInt(r) );
       break;
       
       
                    case "Branch":
                 stmt = conn.prepareStatement("select * from member where Branch = ? ");
                         stmt.setString(1,r );
       break;
       
                    case "Year":
                 stmt = conn.prepareStatement("select * from member where Year = ? ");
                         stmt.setInt(1,Integer.parseInt(r) );
       break;
                }
                ResultSet rs = stmt.executeQuery();
                String s1=new String("Member Id\t\t\tName\t\t\tBranch\t\t\tYear\t\t\tEmail ID\t\t\t\t\tMobile No");
                
                String s="";
                while (rs.next()) {
                    s=s+rs.getInt("Id")+"\t\t\t"+rs.getString("Name")+"\t\t\t"+rs.getString("Branch")+"\t\t\t"+rs.getString("Year")+"\t\t\t"+rs.getString("Email");
                    s=s+"\t\t\t"+rs.getString("Mobile");
                    s=s+"\n\n";   
                }
        
               textArea1 .setText(s1+"\n"+s);
                
            }
            catch(Exception e){}
       
                
    }

