    public Search_result_book(String c,String r) {
        initComponents();
        
        
        
        
        
            Connection conn;
            String JDBC_Driver;
            String DB_URL;
            JDBC_Driver = "com.mysql.jdbc.Driver";
            DB_URL = "jdbc:mysql://localhost:3306/library";
            try {
                Class.forName("com.mysql.jdbc.Driver");
                conn = (Connection) DriverManager.getConnection(DB_URL, "root", "");
                PreparedStatement stmt=null;
                switch(c)
                {
                    case "Name":
                 stmt = conn.prepareStatement("select * from book where Name = ? ");
                         stmt.setString(1,r );
       break;
       
                    case "Author":
                stmt = conn.prepareStatement("select * from book where Author = ? ");
                         stmt.setString(1,r );
       break;
       
       
                    case "Subject":
                 stmt = conn.prepareStatement("select * from book where Subject = ? ");
                         stmt.setString(1,r );
       break;
       
                    case "ISBN":
                 stmt = conn.prepareStatement("select * from book where ISBN = ? ");
                         stmt.setString(1,r );
       break;
                }
       textArea1.setEditable(false);
       
                ResultSet rs = stmt.executeQuery();
                //JLabel l=new JLabel("");
                String s1=new String("Book Id\t\t\tTitle\t\t\tSubject\t\t\tAuthor\t\t\tAuthor\t\t\tAuthor\t\t\tISBN\t\t\t\tEdition\t\t\tPublication\t\t\tNo of Copies");
                
                String s="";
                while (rs.next()) {
                    s=s+rs.getInt("Id")+"\t\t\t"+rs.getString("Title")+"\t\t\t"+rs.getString("Subject")+"\t\t\t"+rs.getString("Author1")+"\t\t\t"+rs.getString("Author2");
                    s=s+"\t\t\t"+rs.getString("Author3")+"\t\t\t"+rs.getString("ISBN")+"\t\t\t\t"+rs.getString("Edition")+"\t\t\t"+rs.getString("Publication")+"\t\t\t"+rs.getInt("No_of_Copies");
                    s=s+"\n\n";   
                }
        
               textArea1.setText(s1+"\n"+s);
                
            }
            catch(Exception e){}
      }
        
        
        
        
        
        
        
        
    

